dado = 10
print(f"Conteúdo da variável dado: {dado}")

dado += 1
print(f"Conteúdo da variável dado após incremento: {dado}")

dado -= 1
print(f"Conteúdo da variável dado após decremento: {dado}")

dado *= 2
print(f"Conteúdo da variável dado após multiplicação: {dado}")

dado /= 2
print(f"Conteúdo da variável dado após divisão: {dado}")