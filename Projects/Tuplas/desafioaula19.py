'''
Crie um programa para emular uma quitanda. Crie umna tupla para armazenar as palavras "maçã", "banana", "laranja". Imprima cada um dos nomes das frutas, utilizando a tupla e uma estrutura do tipo for. Mostre a quantidade de elementos da tupla.
'''
frutas = ("maçã", "banana", "laranja")
for fruta in frutas:
    print(fruta)
print("Quantidade de frutas na tupla: ", len(frutas))